function renderData(data) {
  const $heroList = $("#hero-list");
  const $modalList = $("#modal-list");
  const type = $heroList.data("type");

  var data = [];

  if (type === "anh_hung_tam_ly") {
    data = anhHungTamLy;
  } else if (type === "anh_hung_tu_te") {
    data = anhHungTuTe;
  } else if (type === "cam_nghi_ca_nhan") {
    data = camNghiCaNhan;
  }

  data.filter((hero) => hero.votes_count).map(function (hero, index) {
    let msgCount = 0;
    const addClass = hero.notes.length > 5 ? "smaller-text" : "";
    const messageNotes = hero.notes
      .map((message, index) => {
        let note = message.note.trim();
        note = note.substr(0, 1).toUpperCase() + note.substr(1);
        if (note.length < 6) return '';
        msgCount++;
        return `
              <div class="popup__message wow fadeIn ${addClass}" data-wow-delay="${
          index * 0.3
        }s" data-wow-duration="2s">
                  <div>- ${note}</div>
              </div>
          `;
      })
      .join("");

    if (!msgCount) return;

    const modalHtml = `
                <div class="popup modal fade" id="popup-${index}" tabindex="-1">
                    <div class="modal-dialog modal-dialog-centered">
                        <div class="modal-content">
                            <div class="modal-body">
                                <img src="./images/popup-bg.jpg" alt="" class="popup__bg">
                                <button class="popup__close" type="button" data-bs-dismiss="modal"></button>
                                <div class="popup__content">
                                    <div class="popup__inner">
                                        ${messageNotes}
                                    </div>
                                </div>
                            </div>
                        </div>
                    </div>
                </div>`;
    $modalList.append(modalHtml);

    const heroHtml = `
                <div class="hero">
                    <div class="hero__body wow ${
                      index % 2 ? "fadeInLeft" : "fadeInRight"
                    }">
                        <div class="hero__name">
                            <a href="#!" data-bs-toggle="modal" data-bs-target="#popup-${index}">${
      index + 1
    }. ${hero.title}</a>
                        </div>
                        <div class="hero__pos">Số lượt bình chọn: ${
                          hero.votes_count
                        }</div>
                    </div>
                </div>`;
    $heroList.append(heroHtml);
  });

  var wow = new WOW();
  wow.init();

  $(".popup").on("shown.bs.modal", function () {
    $(this)
      .find(".wow")
      .each(function () {
        wow.show(this);
      });
  });
}

$(function () {
  renderData();
});
